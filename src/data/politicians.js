export const orderedPoliticians = (searchQuery = null) => {
  const formattedSearchQuery = searchQuery ? searchQuery.toLowerCase() : null;
  return politicians
    .sort((a, b) => {
      const textA = a.firstName.toUpperCase();
      const textB = b.firstName.toUpperCase();
      if (textA < textB) {
        return -1;
      }
      return textA > textB ? 1 : 0;
    })
    .filter(
      ({ affiliation, constituency, firstName, lastName, province }) =>
        !formattedSearchQuery ||
        (formattedSearchQuery &&
          (affiliation.toLowerCase().includes(formattedSearchQuery) ||
            constituency.toLowerCase().includes(formattedSearchQuery) ||
            firstName.toLowerCase().includes(formattedSearchQuery) ||
            lastName.toLowerCase().includes(formattedSearchQuery) ||
            province.toLowerCase().includes(formattedSearchQuery)))
    );
};

export const politiciansByProvinceCode = (code, searchQuery = null) => {
  const p = provincesByCode[code];
  return orderedPoliticians(searchQuery).filter(
    ({ province }) => province === p
  );
};

export const politicianImageUrl = (firstName, lastName) => {
  const imgUriLastNameFragment = lastName
    .replace(' ', '')
    .replace("'", '')
    .replace('.', '');

  const imgUriFirstNameFragment = firstName
    .replace(' ', '')
    .replace("'", '')
    .replace('.', '');

  const imgUri = `${imgUriLastNameFragment}${imgUriFirstNameFragment}`;

  return `/images/parliamanterians/${imgUri}.jpg`;
};

